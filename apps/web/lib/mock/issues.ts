export function issueById(id: string) {
  return ISSUES.find((i) => i.id === id);
}

export function issuesByProject(projectId: string) {
  return ISSUES.filter((i) => i.projectId === projectId);
}

export function issuesByCycle(cycleId: string) {
  return ISSUES.filter((i) => i.cycleId === cycleId);
}

export function issuesAssignedTo(userId: string) {
  return ISSUES.filter((i) => i.assigneeIds.includes(userId));
}

export function subIssues(issueId: string) {
  return ISSUES.filter((i) => i.parentId === issueId);
}

export function commentsFor(issueId: string) {
  return COMMENTS.filter((c) => c.issueId === issueId);
}

export function activityFor(issueId: string) {
  return ACTIVITY.filter((a) => a.issueId === issueId);
}
